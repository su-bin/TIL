public class IfExample {
    public static void main(String[] args) {
        int score = 93;

        if(score >= 90) {
            System.out.println("90보다 크다");
            System.out.println("A");
        }

        if(score < 90) {
            System.out.println("90 아래");
            System.out.println("B");
        }
    }

}
