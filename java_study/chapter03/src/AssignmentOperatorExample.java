public class AssignmentOperatorExample {
    public static void main(String[] args) {
        int result = 0;
        result += 10;
        System.out.println("result=" + result);
        result -= 10;
        System.out.println("result=" + result);
        result *= 10;
        System.out.println("result=" + result);
        result /= 10;
        System.out.println("result=" + result);
        result %= 10;
        System.out.println("result=" + result);
    }
}
